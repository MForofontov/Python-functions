def swapcase_string(s: str) -> str:
    """
    Swap the case of each character in a string.

    Parameters
    ----------
    s : str
        The input string.

    Returns
    -------
    str
        The string with each character's case swapped.

    Examples
    --------
    >>> swapcase_string("Hello World")
    'hELLO wORLD'
    >>> swapcase_string("Python")
    'pYTHON'
    """
    return s.swapcase()
